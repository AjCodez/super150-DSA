public class grades {
    public static void main(String[] args) {
        int n=90;
        if (n>=75)
            System.out.println("A");
        else if (n>=65)
            System.out.println("B");
        else if (n>=55)
            System.out.println("C");
        else if (n>=45)
            System.out.println("D");
        else if (n>=40)
            System.out.println("pass");
        else
            System.out.println("Fail");
    }
}
